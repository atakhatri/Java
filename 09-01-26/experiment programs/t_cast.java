public class t_cast {
    public static void main(String[] args) {
        int i1 = 65;
        double d1 = i1;
        System.out.println("i1 = " + i1);
        System.out.println("d1 = " + d1);

        double d2 = 9.78d;
        int i2 = (int) d2;
        System.out.println(d2);
        System.out.println(i2);
    }
}
