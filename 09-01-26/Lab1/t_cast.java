public class t_cast {
    public static void main(String[] args) {
        double num = 2.65d;
        int casted = (int) num;
        System.out.println("Original : " + num);
        System.out.println("After : " + casted);

        System.out.println();

        int num2 = 7;
        double casted2 = num2;
        System.out.println("Original : " + num2);
        System.out.println("After : " + casted2);
    }
}
