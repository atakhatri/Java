public class sumAndCube {
    static void squere(int num)
    {
        System.out.println("squere of given number : " + (num * num));
    }

    static void cube(int num)
    {
        System.out.println("squere of given number : " + (num * num * num));
    }

    public static void main(String[] args) {
        int num = 3;
        cube(num);
        squere(num);
    }
}
